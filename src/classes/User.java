package classes;

/**
 *
 * @author edemko
 */
public class User {
    
    private String name, pass, address, dbName;

    public User(String user, String pass, String address, String dbName) {
        this.name = user;
        this.pass = pass;
        this.address = address;
        this.dbName = dbName;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getDbName() {
        return dbName;
    }

    public void setDbName(String dbName) {
        this.dbName = dbName;
    }
    
    
    
}
